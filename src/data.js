// Helper function to calculate total raised
export const calculateTotalRaised = (contributors) => {
  return contributors.reduce((total, contributor) => total + contributor.amount, 0);
};

// Helper function to format currency
export const formatCurrency = (amount, currency = "KSh") => {
  return new Intl.NumberFormat('en-KE', {
    style: 'currency',
    currency: currency === "KSh" ? 'KES' : 'USD',
    currencyDisplay: currency === "KSh" ? 'code' : 'symbol'
  }).format(amount).replace('KES', 'KSh');
};

// Helper function to calculate progress percentage
export const calculateProgress = (raised, target) => {
  return Math.min((raised / target) * 100, 100);
};
